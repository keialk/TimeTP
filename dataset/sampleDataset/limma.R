library(limma)
library(affy)
setwd("dataset/sampleDataset")
Data <- ReadAffy("GSM679849_17A.CEL","GSM679847_9A.CEL","GSM679848_13A.CEL","GSM679850_10B.CEL","GSM679851_10A.CEL","GSM679853_18A.CEL","GSM679852_14A.CEL")
eset <- rma(Data)
write.exprs(eset, file="Expr_T1.txt")
pData(eset)
design <- model.matrix(~factor(c("C","C","C","C","T","T","T")))
colnames(design) <- c("C","T")
fit <- lmFit(eset, design)
fit <- eBayes(fit)
options(digits=2)
table <- topTable(fit, coef=2, adjust="fdr", n=Inf)
write.table(table, file="../../result/sampleResult/DEG_result.T1.txt", sep="\t", col.names=TRUE, row.names=TRUE)
